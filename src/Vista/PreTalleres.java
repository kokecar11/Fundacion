    private void CargarModificarTaller() {
        if(seleccionTaller!=-1){
            principal.mostrarPanelTalleres();
            principal.CambioTalleres(2);
            principal.CargarTaller((String) jTableTalleres.getValueAt(seleccionTaller, 0));
        }else{
            JOptionPane.showMessageDialog(this, "ELEGIR TALLER");
        }    
    }
    public void cargarTabla() {
        DefaultTableModel modeloTabla= (DefaultTableModel) jTableTalleres.getModel();
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Estado");
        modeloTabla.addColumn("Cupos");
        
        int c=principal.ObtenerNumeroTalleres();
        int x=0;
        Object [][]datos=new Object[c][3];
        datos=principal.CargarTallere();
        while(x<c){
            if(datos[x][1].equals("1")){
                    datos[x][1]="Activo";
            }else{
                datos[x][1]="Inactivo";
            }
            if(datos[x][2].equals("-1")){
                    datos[x][2]="";
            }
            Object row[]= {datos[x][0],datos[x][1],datos[x][2]};
            ((DefaultTableModel)jTableTalleres.getModel()).addRow(row);
            x++;
        }
        
    }
